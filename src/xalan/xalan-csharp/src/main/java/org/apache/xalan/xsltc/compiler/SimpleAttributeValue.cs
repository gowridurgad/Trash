namespace org.apache.xalan.xsltc.compiler
{

	using ConstantPoolGen = org.apache.bcel.generic.ConstantPoolGen;
	using InstructionList = org.apache.bcel.generic.InstructionList;
	using PUSH = org.apache.bcel.generic.PUSH;
	using ClassGenerator = org.apache.xalan.xsltc.compiler.util.ClassGenerator;
	using MethodGenerator = org.apache.xalan.xsltc.compiler.util.MethodGenerator;
	using Type = org.apache.xalan.xsltc.compiler.util.Type;
	using TypeCheckError = org.apache.xalan.xsltc.compiler.util.TypeCheckError;

	/// <summary>
	/// @author Jacek Ambroziak
	/// @author Santiago Pericas-Geertsen
	/// </summary>
	internal sealed class SimpleAttributeValue : AttributeValue
	{

		private string _value; // The attributes value (literate string).

		/// <summary>
		/// Creates a new simple attribute value. </summary>
		/// <param name="value"> the attribute value. </param>
		public SimpleAttributeValue(string value)
		{
		_value = value;
		}

		/// <summary>
		/// Returns this attribute value's type (String). </summary>
		/// <param name="stable"> The compiler/parser's symbol table </param>
//JAVA TO C# CONVERTER WARNING: Method 'throws' clauses are not available in .NET:
//ORIGINAL LINE: public org.apache.xalan.xsltc.compiler.util.Type typeCheck(SymbolTable stable) throws org.apache.xalan.xsltc.compiler.util.TypeCheckError
		public override Type typeCheck(SymbolTable stable)
		{
		return _type = Type.String;
		}

		public override string ToString()
		{
		return _value;
		}

		protected internal override bool contextDependent()
		{
		return false;
		}

		/// <summary>
		/// Translate this attribute value into JVM bytecodes that pushes the
		/// attribute value onto the JVM's stack. </summary>
		/// <param name="classGen"> BCEL Java class generator </param>
		/// <param name="methodGen"> BCEL Java method generator </param>
		public override void translate(ClassGenerator classGen, MethodGenerator methodGen)
		{
//JAVA TO C# CONVERTER WARNING: The original Java variable was marked 'final':
//ORIGINAL LINE: final org.apache.bcel.generic.ConstantPoolGen cpg = classGen.getConstantPool();
		ConstantPoolGen cpg = classGen.ConstantPool;
//JAVA TO C# CONVERTER WARNING: The original Java variable was marked 'final':
//ORIGINAL LINE: final org.apache.bcel.generic.InstructionList il = methodGen.getInstructionList();
		InstructionList il = methodGen.InstructionList;
		il.append(new PUSH(cpg, _value));
		}
	}

}